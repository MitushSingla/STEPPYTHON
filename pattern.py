print("Patterns in python :")
for x in range(10):
    print(x)
print("Simple pyramid pattern :")
for x in range(4):
    for y in range(0,x+1):
        print("*",end="")
    print("\r")
print("Square Pattern")
for x in range(4):
    for y in range(4):
        print("*",end="")
    print("\r")